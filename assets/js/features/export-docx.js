import {
  Document,
  Packer,
  Paragraph,
  TextRun,
  HeadingLevel,
} from "docx";

function value(id, fallback = "-") {
  return document.querySelector(id)?.value || fallback;
}

export async function exportMeetingDOCX() {
  const title = value("#meetingTitle", "Notulen Rapat");

  const doc = new Document({
    sections: [
      {
        children: [
          new Paragraph({
            text: title,
            heading: HeadingLevel.TITLE,
          }),
          new Paragraph(`Tanggal: ${value("#meetingDate")}`),
          new Paragraph(`Peserta: ${value("#participants")}`),
          new Paragraph({
            text: "Ringkasan",
            heading: HeadingLevel.HEADING_1,
          }),
          new Paragraph(value("#summary")),
          new Paragraph({
            text: "Keputusan",
            heading: HeadingLevel.HEADING_1,
          }),
          new Paragraph(value("#decisions")),
          new Paragraph({
            text: "Action Items",
            heading: HeadingLevel.HEADING_1,
          }),
          new Paragraph(value("#actionItems")),
          new Paragraph({
            children: [
              new TextRun({
                text: "Generated by FieldPress AI",
                italics: true,
              }),
            ],
          }),
        ],
      },
    ],
  });

  const blob = await Packer.toBlob(doc);
  const url = URL.createObjectURL(blob);

  const a = document.createElement("a");
  a.href = url;
  a.download = `${title.replace(/[^a-z0-9]+/gi, "-").toLowerCase()}.docx`;
  a.click();

  URL.revokeObjectURL(url);
}
